package com.njwb.joybeans.pojo;

import java.util.Date;

import com.njwb.joybeans.util.DateUtil;

/**
 * 游戏实体类
 * 
 * @author soft01
 * 
 */
public class Game {
	/**
	 * 游戏ID
	 */
	private String id;

	/**
	 * 游戏名
	 */
	private String gameName;

	/**
	 * 游戏类型ID
	 */
	private String gameType;

	/**
	 * 游戏类型名称
	 */
	private String typeName;

	/**
	 * 游戏图片
	 */
	private String gamePicture;

	/**
	 * 游戏详情
	 */
	private String gameDetail;

	/**
	 * 游戏状态
	 */
	private String gameStatus;

	/**
	 * 话费价格
	 */
	private String gameFee;

	/**
	 * 乐豆价格
	 */
	private String gameBeans;

	/**
	 * 上次修改时间
	 */
	private Date modifyTime;

	/**
	 * 创建时间
	 */
	private Date createTime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getGameName() {
		return gameName;
	}

	public void setGameName(String gameName) {
		this.gameName = gameName;
	}

	public String getGameType() {
		return gameType;
	}

	public void setGameType(String gameType) {
		this.gameType = gameType;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public String getGamePicture() {
		return gamePicture;
	}

	public void setGamePicture(String gamePicture) {
		this.gamePicture = gamePicture;
	}

	public String getGameDetail() {
		return gameDetail;
	}

	public void setGameDetail(String gameDetail) {
		this.gameDetail = gameDetail;
	}

	public String getGameStatus() {
		return gameStatus;
	}

	public void setGameStatus(String gameStatus) {
		this.gameStatus = gameStatus;
	}

	public String getGameFee() {
		return gameFee;
	}

	public void setGameFee(String gameFee) {
		this.gameFee = gameFee;
	}

	public String getGameBeans() {
		return gameBeans;
	}

	public void setGameBeans(String gameBeans) {
		this.gameBeans = gameBeans;
	}

	public String getModifyTime() {
		return DateUtil.date2Str(modifyTime, "yyyy-MM-dd");
	}

	public void setModifyTime(Date modifyTime) {
		this.modifyTime = modifyTime;
	}

	public String getCreateTime() {
		return DateUtil.date2Str(createTime, "yyyy-MM-dd");
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

}
