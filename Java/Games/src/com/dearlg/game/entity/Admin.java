package com.dearlg.game.entity;

/**
 * 管理员类
 * 
 * @author soft01
 * 
 */
public class Admin {
	/**
	 * 管理员的ID
	 */
	private String id;

	/**
	 * 管理员的密码
	 */
	private String password;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
